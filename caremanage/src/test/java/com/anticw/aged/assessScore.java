package com.anticw.aged;

public enum assessScore {
	PRO(4,5, 5);
	int core1;
	int core2;
	int core3;
	private assessScore(int core1, int core2, int core3) {
		this.core1 = core1;
		this.core2 = core2;
		this.core3 = core3;
	}
	public int getCore1() {
		return core1;
	}
	public void setCore1(int core1) {
		this.core1 = core1;
	}
	public int getCore2() {
		return core2;
	}
	public void setCore2(int core2) {
		this.core2 = core2;
	}
	public int getCore3() {
		return core3;
	}
	public void setCore3(int core3) {
		this.core3 = core3;
	}
	
	
	
}
