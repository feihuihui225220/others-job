package com.anticw.aged.bean.mongo;

import org.springframework.data.mongodb.core.mapping.Document;

/**
 * 心脏频谱服务数据表
 * <P>File name : Heart.java </P>
 * <P>Author : zhouyanxin </P> 
 * <P>Date : 2014-10-11 </P>
 */
@Document(collection="m_heart")
public class Heart extends MetaData {
	
	/**
	 * 心率频谱（脉搏频率）
	 */
	private Float pulseRate;
	
	/**
	 * 瓣膜频谱
	 */
	private Float valueRate;
	
	/**
	 * 心血管频谱（血压心率）
	 */
	private Float bloodRate;

	/**
	 * @return Float 取得域 pulseRate 的方法。
	 */
	public Float getPulseRate() {
		return pulseRate;
	}

	/**
	 * @param pulseRate 对域 pulseRate 的设置方法.
	 */
	public void setPulseRate(Float pulseRate) {
		this.pulseRate = pulseRate;
	}

	/**
	 * @return Float 取得域 valueRate 的方法。
	 */
	public Float getValueRate() {
		return valueRate;
	}

	/**
	 * @param valueRate 对域 valueRate 的设置方法.
	 */
	public void setValueRate(Float valueRate) {
		this.valueRate = valueRate;
	}

	/**
	 * @return String 取得域 bloodRate 的方法。
	 */
	public Float getBloodRate() {
		return bloodRate;
	}

	/**
	 * @param bloodRate 对域 bloodRate 的设置方法.
	 */
	public void setBloodRate(Float bloodRate) {
		this.bloodRate = bloodRate;
	}
}
