package com.anticw.aged.bean.mongo;

import org.springframework.data.mongodb.core.mapping.Document;

/**
 * 血氧管理服务数据表
 * <P>File name : Oxygen.java </P>
 * <P>Author : zhouyanxin </P> 
 * <P>Date : 2014-10-11 </P>
 */
@Document(collection="m_oxygen")
public class Oxygen extends MetaData {

	/**
	 * 血氧饱和对（Spo2）
	 */
	private Float spo2;

	/**
	 * @return String 取得域 spo2 的方法。
	 */
	public Float getSpo2() {
		return spo2;
	}

	/**
	 * @param spo2 对域 spo2 的设置方法.
	 */
	public void setSpo2(Float spo2) {
		this.spo2 = spo2;
	}
}
