package com.anticw.aged.bean.mongo;

import org.springframework.data.mongodb.core.mapping.Document;

/**
 * 心电管理服务数据表
 * <P>File name : Ecg.java </P>
 * <P>Author : zhouyanxin </P> 
 * <P>Date : 2014-10-11 </P>
 */
@Document(collection="m_ecg")
public class Ecg extends MetaData {

	/**
	 * HR
	 */
	private String hr;
	
	/**
	 * QRS
	 */
	private Float qrs;
	
	/**
	 * ST
	 */
	private Float st;
	
	/**
	 * 波形
	 */
	private Float wave;

	/**
	 * @return String 取得域 hr 的方法。
	 */
	public String getHr() {
		return hr;
	}

	/**
	 * @param hr 对域 hr 的设置方法.
	 */
	public void setHr(String hr) {
		this.hr = hr;
	}

	/**
	 * @return Float 取得域 qrs 的方法。
	 */
	public Float getQrs() {
		return qrs;
	}

	/**
	 * @param qrs 对域 qrs 的设置方法.
	 */
	public void setQrs(Float qrs) {
		this.qrs = qrs;
	}

	/**
	 * @return Float 取得域 st 的方法。
	 */
	public Float getSt() {
		return st;
	}

	/**
	 * @param st 对域 st 的设置方法.
	 */
	public void setSt(Float st) {
		this.st = st;
	}

	/**
	 * @return Float 取得域 wave 的方法。
	 */
	public Float getWave() {
		return wave;
	}

	/**
	 * @param wave 对域 wave 的设置方法.
	 */
	public void setWave(Float wave) {
		this.wave = wave;
	}
}
