package com.anticw.aged.bean;

import static javax.persistence.GenerationType.IDENTITY;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "r_nuser")
public class RNuser {
	@Id
	@GeneratedValue(strategy = IDENTITY)
	@Column(name = "id", unique = true)
	private Integer id;
	@Column(name = "user_id")
	private Long userId;
	@Column(name = "n_name")
	private String nName;
	@Column(name = "n_age")
	private Integer nAge;
	@Column(name = "n_sex")
	private String nSex;
	@Column(name = "n_relation")
	private String nRelation;
	@Column(name = "n_addr")
	private String nAddr;
	@Column(name = "n_phone")
	private String nPhone;
	@Column(name = "n_mobilePhone")
	private String nMobilePhone;
	@Column(name = "n_content")
	private String nContent;
	@Column(name = "n_timedate")
	private String nTimedate;
	@Column(name = "n_liveTogether")
	private String nliveTogether;
	@Column(name = "n_state")
	private String nState;
	@Column(name = "careate_at")
	private Date careateAt;
	@Column(name = "careate_by")
	private String careateBy;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public String getnName() {
		return nName;
	}
	public void setnName(String nName) {
		this.nName = nName;
	}

	
	public Integer getnAge() {
		return nAge;
	}
	public void setnAge(Integer nAge) {
		this.nAge = nAge;
	}
	public String getnSex() {
		return nSex;
	}
	public void setnSex(String nSex) {
		this.nSex = nSex;
	}
	public String getnRelation() {
		return nRelation;
	}
	public void setnRelation(String nRelation) {
		this.nRelation = nRelation;
	}
	public String getnAddr() {
		return nAddr;
	}
	public void setnAddr(String nAddr) {
		this.nAddr = nAddr;
	}
	public String getnPhone() {
		return nPhone;
	}
	public void setnPhone(String nPhone) {
		this.nPhone = nPhone;
	}
	public String getnMobilePhone() {
		return nMobilePhone;
	}
	public void setnMobilePhone(String nMobilePhone) {
		this.nMobilePhone = nMobilePhone;
	}
	public String getnContent() {
		return nContent;
	}
	public void setnContent(String nContent) {
		this.nContent = nContent;
	}
	public String getnTimedate() {
		return nTimedate;
	}
	public void setnTimedate(String nTimedate) {
		this.nTimedate = nTimedate;
	}
	public String getNliveTogether() {
		return nliveTogether;
	}
	public void setNliveTogether(String nliveTogether) {
		this.nliveTogether = nliveTogether;
	}
	public String getnState() {
		return nState;
	}
	public void setnState(String nState) {
		this.nState = nState;
	}
	public Date getCareateAt() {
		return careateAt;
	}
	public void setCareateAt(Date careateAt) {
		this.careateAt = careateAt;
	}
	public String getCareateBy() {
		return careateBy;
	}
	public void setCareateBy(String careateBy) {
		this.careateBy = careateBy;
	}
	
	
	
}
