package com.anticw.aged.bean;

import static javax.persistence.GenerationType.IDENTITY;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;


@Entity
@Table(name = "m_material", catalog = "")
public class MMaterial {
	@Id
	@GeneratedValue(strategy = IDENTITY)
	@Column(name = "id", unique = true, nullable = false)
	private int id;
	
	@Column(name = "material_name")
	private String materialName;
	
	@Column(name = "material_amount")
	private String materialAmount;
	
	@Column(name = "material_nature")
	private String materialNature;
	
	@Column(name = "material_standard")
	private String materialStandard;
	
	@Column(name = "material_wight")
	private String materialWight;
	
	@Column(name = "material_height")
	private String materialHeight;
	
	@Column(name = "material_length")
	private String materialLength;
	
	@Column(name = "material_color")
	private String materialColor;
	
	@Column(name = "img_url")
	private String imgUrl;
	
	@Column(name = "material_feature")
	private String materialFeature;
	
	@Column(name = "program_type")
	private Integer programType;
	
	@Column(name = "vender_id")
	private Integer venderId;
	
	@Column(name = "created_at")
	private Date createdAt;
	
	@Column(name = "created_by")
	private String createdBy;
	
	@Column(name = "changed_at")
	private Date changedAt;
	
	@Column(name = "changed_by")
	private String changedBy;
	
	@Column(name = "version")
	private Short version;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMaterialName() {
		return materialName;
	}
	public void setMaterialName(String materialName) {
		this.materialName = materialName;
	}
	public String getMaterialAmount() {
		return materialAmount;
	}
	public void setMaterialAmount(String materialAmount) {
		this.materialAmount = materialAmount;
	}
	public String getMaterialNature() {
		return materialNature;
	}
	public void setMaterialNature(String materialNature) {
		this.materialNature = materialNature;
	}
	public String getMaterialStandard() {
		return materialStandard;
	}
	public void setMaterialStandard(String materialStandard) {
		this.materialStandard = materialStandard;
	}
	public String getMaterialWight() {
		return materialWight;
	}
	public void setMaterialWight(String materialWight) {
		this.materialWight = materialWight;
	}
	public String getMaterialHeight() {
		return materialHeight;
	}
	public void setMaterialHeight(String materialHeight) {
		this.materialHeight = materialHeight;
	}
	public String getMaterialLength() {
		return materialLength;
	}
	public void setMaterialLength(String materialLength) {
		this.materialLength = materialLength;
	}
	public String getMaterialColor() {
		return materialColor;
	}
	public void setMaterialColor(String materialColor) {
		this.materialColor = materialColor;
	}
	public String getImgUrl() {
		return imgUrl;
	}
	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}
	public String getMaterialFeature() {
		return materialFeature;
	}
	public void setMaterialFeature(String materialFeature) {
		this.materialFeature = materialFeature;
	}
	
	public Integer getProgramType() {
		return programType;
	}
	public void setProgramType(Integer programType) {
		this.programType = programType;
	}
	
	
	
	public Integer getVenderId() {
		return venderId;
	}
	public void setVenderId(Integer venderId) {
		this.venderId = venderId;
	}
	public Date getCreatedAt() {
		return createdAt;
	}
	public void setCreatedAt(Date createdAt) {
		this.createdAt = createdAt;
	}
	public void setChangedAt(Date changedAt) {
		this.changedAt = changedAt;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	
	public String getChangedBy() {
		return changedBy;
	}
	public void setChangedBy(String changedBy) {
		this.changedBy = changedBy;
	}
	public Short getVersion() {
		return version;
	}
	public void setVersion(Short version) {
		this.version = version;
	}
	public Date getChangedAt() {
		return changedAt;
	}
	
	
	
}
