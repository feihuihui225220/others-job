public class VolunteerVO {
    private Integer id;
    private String name;
    private String sex;
    private Integer age;
    private String contact;
    private String community;
    private Integer siteId;
    private String siteNmae;
    private String communityNmae;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getCommunity() {
        return community;
    }

    public void setCommunity(String community) {
        this.community = community;
    }


	public Integer getSiteId() {
		return siteId;
	}

	public void setSiteId(Integer siteId) {
		this.siteId = siteId;
	}

	public String getSiteNmae() {
		return siteNmae;
	}

	public void setSiteNmae(String siteNmae) {
		this.siteNmae = siteNmae;
	}

	public String getCommunityNmae() {
		return communityNmae;
	}

	public void setCommunityNmae(String communityNmae) {
		this.communityNmae = communityNmae;
	}
    
	
    
}
