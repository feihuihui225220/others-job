package com.anticw.aged.vo.professional.vender.state;

/**   
 * 移动端用户状态VO
 * @ClassName: MobileStateVo     
 * @author zhoupeipei
 * @Date 2014-10-23 上午1:28:14      
 */

public class MobileStateVo {
	/**  
	 * 状态标题。
	 */
	private String title;
	/**  
	 * 状态值。
	 */
	private String value;
	/**  
	 * 状态描述。
	 */
	private String detail;
	
	/**  
	 * return String 获取域title的方法。
	 */
	public String getTitle() {
		return title;
	}
	
	/**  
	 * param String 设置域title的方法。  
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	
	/**  
	 * return String 获取域value的方法。
	 */
	public String getValue() {
		return value;
	}
	
	/**  
	 * param String 设置域value的方法。  
	 */
	public void setValue(String value) {
		this.value = value;
	}
	
	/**  
	 * return String 获取域detail的方法。
	 */
	public String getDetail() {
		return detail;
	}
	
	/**  
	 * param String 设置域detail的方法。  
	 */
	public void setDetail(String detail) {
		this.detail = detail;
	}

}
