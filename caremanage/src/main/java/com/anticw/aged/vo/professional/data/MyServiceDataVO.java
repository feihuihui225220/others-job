public class MyServiceDataVO {

	/**
	 * 服务Id
	 */
	private Integer serviceId;
	
	/**
	 * 服务名称
	 */
	private String serviceName;
	
	/**
	 * 元服务Id
	 */
	private Integer metaServiceId;
	/**
	 * 展现方式
	 */
	private Integer stype;
	
	/**
	 * 数据集
	 */
	private CommonDataVO data;
	
	/**
	 * 数据上传日期
	 */
	private Date date;
	
	/**
	 * 展现方式
	 */
	private Short type;

	/**
	 * @return Integer 取得域 serviceId 的方法。
	 */
	public Integer getServiceId() {
		return serviceId;
	}

	/**
	 * @param serviceId 对域 serviceId 的设置方法.
	 */
	public void setServiceId(Integer serviceId) {
		this.serviceId = serviceId;
	}

	/**
	 * @return String 取得域 serviceName 的方法。
	 */
	public String getServiceName() {
		return serviceName;
	}

	/**
	 * @param serviceName 对域 serviceName 的设置方法.
	 */
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}

	/**
	 * @return CommonDataVO 取得域 data 的方法。
	 */
	public CommonDataVO getData() {
		return data;
	}

	/**
	 * @param data 对域 data 的设置方法.
	 */
	public void setData(CommonDataVO data) {
		this.data = data;
	}

	/**
	 * @return Integer 取得域 metaServiceId 的方法。
	 */
	public Integer getMetaServiceId() {
		return metaServiceId;
	}

	/**
	 * @param metaServiceId 对域 metaServiceId 的设置方法.
	 */
	public void setMetaServiceId(Integer metaServiceId) {
		this.metaServiceId = metaServiceId;
	}

	/**
	 * @return the stype
	 */
	public Integer getStype() {
		return stype;
	}

	/**
	 * @param stype the stype to set
	 */
	public void setStype(Integer stype) {
		this.stype = stype;
	}

	/**
	 * @return the date
	 */
	public Date getDate() {
		return date;
	}

	/**
	 * @param date the date to set
	 */
	public void setDate(Date date) {
		this.date = date;
	}

	public Short getType() {
		return type;
	}

	public void setType(Short type) {
		this.type = type;
	}
	
	
}
