package com.anticw.aged.vo.professional.activity;

public class StatisticsVO {

	private String SiteId;
	private String SiteName;
	private String Sumw; //未排课活动数量
	private String Sumz; //所有的活动数量
	private String STime;//总课时
	private String WPlan;//完成的排课数量
	private String WTime;//完成的时间
	
	public String getSiteId() {
		return SiteId;
	}
	public void setSiteId(String siteId) {
		SiteId = siteId;
	}
	public String getSiteName() {
		return SiteName;
	}
	public void setSiteName(String siteName) {
		SiteName = siteName;
	}

	public String getSumw() {
		return Sumw;
	}
	public void setSumw(String sumw) {
		Sumw = sumw;
	}
	public String getSumz() {
		return Sumz;
	}
	public void setSumz(String sumz) {
		Sumz = sumz;
	}
	public String getSTime() {
		return STime;
	}
	public void setSTime(String sTime) {
		STime = sTime;
	}
	public String getWPlan() {
		return WPlan;
	}
	public void setWPlan(String wPlan) {
		WPlan = wPlan;
	}
	public String getWTime() {
		return WTime;
	}
	public void setWTime(String wTime) {
		WTime = wTime;
	}
	
}
