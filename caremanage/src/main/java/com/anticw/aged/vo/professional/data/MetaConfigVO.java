package com.anticw.aged.vo.professional.data;

/**
 * 元服务数据的配置抽象类
 * <P>File name : MetaConfigVO.java </P>
 * <P>Author : zhouyanxin </P> 
 * <P>Date : 2014-10-12 </P>
 */
public class MetaConfigVO {

	/**
	 * 报表的Id
	 * 报表的名称
	 */
	private String id;
	
	/**
	 * 获取改报表数据的service名称
	 */
	private String beanName;
	
	/**
	 * 获取改报表数据的service的全限定名
	 */
	private String classFullQuanlifiedName;
	
	/**
	 * 构造函数
	 */
	public MetaConfigVO(String id, String beanName, String classFullQuanlifiedName) {
		this.id = id;
		this.beanName = beanName;
		this.classFullQuanlifiedName = classFullQuanlifiedName;
	}

	/**
	 * @return String 取得域 id 的方法。
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id 对域 id 的设置方法.
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return String 取得域 beanName 的方法。
	 */
	public String getBeanName() {
		return beanName;
	}

	/**
	 * @param beanName 对域 beanName 的设置方法.
	 */
	public void setBeanName(String beanName) {
		this.beanName = beanName;
	}

	/**
	 * @return String 取得域 classFullQuanlifiedName 的方法。
	 */
	public String getClassFullQuanlifiedName() {
		return classFullQuanlifiedName;
	}

	/**
	 * @param classFullQuanlifiedName 对域 classFullQuanlifiedName 的设置方法.
	 */
	public void setClassFullQuanlifiedName(String classFullQuanlifiedName) {
		this.classFullQuanlifiedName = classFullQuanlifiedName;
	}

	@Override
	public String toString() {
		return "MetaConfigVO [id=" + id + ", beanName=" + beanName
				+ ", classFullQuanlifiedName=" + classFullQuanlifiedName + "]";
	}
	
	
}
