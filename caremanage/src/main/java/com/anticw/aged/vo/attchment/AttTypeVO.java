package com.anticw.aged.vo.attchment;

/**
 * 附件类型
 * <P>File name : AttTypeVO.java </P>
 * <P>Author : zhouyanxin </P> 
 * <P>Date : 2014-9-28 </P>
 */
public class AttTypeVO {

	/**
	 * 附件类型Id
	 */
	private int id;
	
	/**
	 * 附件类型名称
	 */
	private String name;
	
	/**
	 * 附件对应的相对路径
	 */
	private String uri;

	/**
	 * @return int 取得域 id 的方法。
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id 对域 id 的设置方法.
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return String 取得域 name 的方法。
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name 对域 name 的设置方法.
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return String 取得域 uri 的方法。
	 */
	public String getUri() {
		return uri;
	}

	/**
	 * @param uri 对域 uri 的设置方法.
	 */
	public void setUri(String uri) {
		this.uri = uri;
	}
}
